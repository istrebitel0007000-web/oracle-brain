SUPPORTED_LANGUAGES: list[str] = list(TRANSLATIONS.keys())
LANGUAGE_NAMES: dict[str, str] = {
    "en": "English",
    "ar": "العربية",
    "ru": "Русский",
    "uz": "O'zbek",
}
# Alias
LANGUAGE_LABELS = LANGUAGE_NAMES
RTL_LANGUAGES: set[str] = {"ar"}


def t(key: str, lang: str = "en", **kwargs) -> str:
    """Translate key to lang, with optional str.format() kwargs."""
    lang = lang if lang in TRANSLATIONS else "en"
    text = TRANSLATIONS[lang].get(key) or TRANSLATIONS["en"].get(key, key)
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, ValueError):
            pass
    return text


def is_rtl(lang: str) -> bool:
    return lang in RTL_LANGUAGES


def get_html_dir(lang: str) -> str:
    """Return 'rtl' for right-to-left languages, 'ltr' otherwise."""
    return "rtl" if is_rtl(lang) else "ltr"


def get_lang_label(lang: str) -> str:
    return LANGUAGE_NAMES.get(lang, lang.upper())
